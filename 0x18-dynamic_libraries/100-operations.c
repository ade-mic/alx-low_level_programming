#include <stdio.h>
/**
 * add - addition of two integers
 * @a: first integers
 * @b: second integers
 * Return: addition of the two integer
 */
int add(int a, int b)
{
	return ((a) + (b));
}

/**
 * sub - addition of two integers
 * @a: first integers
 * @b: second integers
 * Return: addition of the two integer
 */
int sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * mul - addition of two integers
 * @a: first integers
 * @b: second integers
 * Return: addition of the two integer
 */
int mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * div - addition of two integers
 * @a: first integers
 * @b: second integers
 * Return: addition of the two integer
 */
int div(int a, int b)
{
	return ((a) / (b));
}

/**
 * mod - addition of two integers
 * @a: first integers
 * @b: second integers
 * Return: addition of the two integer
 */
int mod(int a, int b)
{
	return ((a) % (b));
}
